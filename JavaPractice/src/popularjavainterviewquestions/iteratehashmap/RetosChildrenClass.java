package popularjavainterviewquestions.iteratehashmap;

public class RetosChildrenClass {

    String name;
    int age;
    boolean behave;
    char shirtSize;
    String favoriteFood;
    long salary;
    String futurePosition;


    public RetosChildrenClass(String name, int age, boolean behave, char shirtSize, String favoriteFood, long salary, String futurePosition) {
        this.name = name;
        this.age = age;
        this.behave = behave;
        this.shirtSize = shirtSize;
        this.favoriteFood = favoriteFood;
        this.salary = salary;
        this.futurePosition = futurePosition;
    }


    public RetosChildrenClass(String name, int age, boolean behave){
        this.name = name;
        this.age = age;
        this.behave = behave;
    }


}
